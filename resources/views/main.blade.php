                            <p>1. <strong>Лига </strong>- это соревнование в пределах одного региона (например, Лига &quot;Астана&quot; или Лига &quot;Сокольники&quot;), которые всегда открыты для всех желающих.<br />
                                2. <strong>Клубы</strong> - это соревнование в рамках конкретной организации или группы людей (например, &quot;Минский клуб фанатов Роджера Федерера&quot;). Точно так же как и в Facebook или Вконтакте, Клубы могут быть как Открытыми (любой желающий может вступить в один клик), так и Закрытыми (Организатор Клуба должен подтвердить запрос на вступление нового участника). Каждый игрок может быть участником нескольких клубов.<br />
                                3. <strong>Турниры</strong> - это соревнования&nbsp;игроков разного пола, возраста или уровня, которые были приглашены Организатором турнира. По-сравнению с Лигами или Клубами, игроки получают больше очков за игры и победы в турнирах.<br />
                                4. <strong>Найти партнера </strong>- этот вариант доступен для тех, кто не еще не готов или не хочет играть в соревновательный теннис, а просто ищет партнера с кем можно сыграть не на счет и без отправки вызовов.</p>
